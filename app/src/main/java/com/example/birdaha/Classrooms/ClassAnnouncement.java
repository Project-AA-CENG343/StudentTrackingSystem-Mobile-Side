package com.example.birdaha.Classrooms;

/**
 * Represents an announcement for a class.
 */
public class ClassAnnouncement {
    /**
     * The title of the class announcement.
     */
    private String title;
    /**
     * The details or content of the class announcement.
     */
    private String details;

    /**
     * Gets the title of the class announcement.
     *
     * @return The title of the class announcement.
     */
    public String getTitle() {
        return title;
    }

    /**
     * Sets the title of the class announcement.
     *
     * @param title The title to set for the class announcement.
     */
    public void setTitle(String title) {
        this.title = title;
    }

}
